package com.sogou.bizwork.task.api.core.activiti.dto;

public class RepositoryFormData {
	private String repositoryName;
	private String manager;
	private String description;
	private boolean needADPublish;
	private boolean needFinance;
	private boolean need404;
	private String remark;
	public String getRepositoryName() {
		return repositoryName;
	}
	public void setRepositoryName(String repositoryName) {
		this.repositoryName = repositoryName;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public boolean isNeedADPublish() {
		return needADPublish;
	}
	public void setNeedADPublish(boolean needADPublish) {
		this.needADPublish = needADPublish;
	}
	public boolean isNeedFinance() {
		return needFinance;
	}
	public void setNeedFinance(boolean needFinance) {
		this.needFinance = needFinance;
	}
	public boolean isNeed404() {
		return need404;
	}
	public void setNeed404(boolean need404) {
		this.need404 = need404;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
