package com.sogou.bizwork.task.api.core.taskfollow.po;

/**
 * @description 任务关注实体类,对应数据库表中的t_task_follow
 * @author liquancai
 * @date 2016年7月21日
 * @version 1.0.0
 */
public class TaskFollow {
	private long id;			
	private long taskId;				//任务ID
	private long followUser;			//任务关注人ID
	private byte status;			//状态(0有效,-1删除)
	private String createTime;		//创建时间
	private String changeTime;		//修改时间
	public long getId() {
    	return id;
    }
	public void setId(long id) {
    	this.id = id;
    }
	public long getTaskId() {
    	return taskId;
    }
	public void setTaskId(long taskId) {
    	this.taskId = taskId;
    }
	public long getFollowUser() {
    	return followUser;
    }
	public void setFollowUser(long followUser) {
    	this.followUser = followUser;
    }
	public byte getStatus() {
    	return status;
    }
	public void setStatus(byte status) {
    	this.status = status;
    }
	public String getCreateTime() {
    	return createTime;
    }
	public void setCreateTime(String createTime) {
    	this.createTime = createTime;
    }
	public String getChangeTime() {
    	return changeTime;
    }
	public void setChangeTime(String changeTime) {
    	this.changeTime = changeTime;
    }
	
}
