package com.sogou.bizwork.task.api.core.score.bo;

public class ScoreTask {
	private String taskName;
	private int score;
	private String scoreType;
	private String reason;
	private String givePeople;
	private String updateTime;
	private short scoreFrom;  //0:leader添加、1:积分任务获得
	
	public String getTaskName() {
		return taskName;
	}
	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getScoreType() {
		return scoreType;
	}
	public void setScoreType(String scoreType) {
		this.scoreType = scoreType;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getGivePeople() {
		return givePeople;
	}
	public void setGivePeople(String givePeople) {
		this.givePeople = givePeople;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public short getScoreFrom() {
		return scoreFrom;
	}
	public void setScoreFrom(short scoreFrom) {
		this.scoreFrom = scoreFrom;
	}
	
}
