package com.sogou.bizwork.task.api.web.tasklog.vo;

import java.io.Serializable;

/**
 * @description 任务操作日志Vo
 * @author liquancai
 * @date 2016年8月8日
 */
public class TaskLogVo implements Serializable{
    private static final long serialVersionUID = 7769056552700155814L;
    
    private String operateUserName;				//操作用户名称
    private String operateDescription; 			//操作详情
    private String createTime; 					//操作时间
    
	public String getOperateUserName() {
    	return operateUserName;
    }
	public void setOperateUserName(String operateUserName) {
    	this.operateUserName = operateUserName;
    }
	public String getOperateDescription() {
    	return operateDescription;
    }
	public void setOperateDescription(String operateDescription) {
    	this.operateDescription = operateDescription;
    }
	public String getCreateTime() {
    	return createTime;
    }
	public void setCreateTime(String createTime) {
    	this.createTime = createTime;
    }
}
