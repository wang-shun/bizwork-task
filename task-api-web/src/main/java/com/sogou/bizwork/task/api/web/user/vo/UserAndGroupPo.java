package com.sogou.bizwork.task.api.web.user.vo;

import java.io.Serializable;

/**
 * 用于前端展示使用
 *
 */
public class UserAndGroupPo implements Serializable {

    private static final long serialVersionUID = 1895873147264286038L;

    private long id; // employee_id
    private String name; // username  or groupname depend on type
    private String label; // chinesename  or groupname depend on type
    private int type;// 关注者类型，0：User 1：group

    public int getType() {
        return type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setType(int type) {
        this.type = type;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String chinesename) {
        this.label = chinesename;
    }

}
